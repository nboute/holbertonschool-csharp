using System;

namespace MyMath
{
	/// <summary>
	/// Contains operations for performing basic math operations
	/// </summary>
    public class Operations
    {
		/// <summary>
		/// Adds two integers
		/// </summary>
		/// <param name="a"></param>
		/// <param name="b"></param>
		/// <returns></returns>
		public static int Add(int a, int b)
		{
			return a + b;
		}
    }
}
